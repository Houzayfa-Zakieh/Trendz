////////////////////////////////////////////  1  /////////////////////////////////////////////////////// 

var drag1 = document.querySelector('.dragbtn1');
var scrollDrag1 = document.querySelector('.drag1');
var card1 = document.querySelector('.card1');

drag1.onclick = () => {
    scrollDrag1.classList.toggle('show');
    if (card1.classList.contains('rotate')) {
        card1.classList.remove('rotate');

    }

};
var moreDetails1 = document.querySelector('.moreDetails1');
moreDetails1.onclick = () => {
    card1.classList.toggle('rotate');
};
var colors = document.querySelectorAll('.colors1 .color');
var front1 = document.querySelector('.front1')
colors.forEach((color) => {
    color.addEventListener('click', () => {
        front1.style.backgroundImage = "url(" + color.getAttribute('data-image') + ")";
    });
});

